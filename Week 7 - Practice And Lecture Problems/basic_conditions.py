# Basic Condition Statements demo - Lcoules

variable1 = 24
variable2 = "string value of some sort"
variable3 = 3.14159

if type(variable1) == int:
    print("The variable is an integer.")

if type(variable2) == str:
    print("The variable is a string.")

if type(variable3) == float:
    print("The variable is floating point type.")
else:
    print("The variable is something else.")